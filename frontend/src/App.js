const contractAddress = "0xe7f1725e7734ce288f8367e1bb143e90bb3f0512"; // Replace with your deployed address

function App() {
  const [account, setAccount] = useState(null);
  const [recipient, setRecipient] = useState("");
  const [uri, setUri] = useState("");
  const [issueStatus, setIssueStatus] = useState("");
  const [verifyId, setVerifyId] = useState("");
  const [verifyResult, setVerifyResult] = useState(null);

  // Connect to MetaMask
  const connectWallet = async () => {
    if (window.ethereum) {
      const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
      setAccount(accounts[0]);
    } else {
      alert("MetaMask not detected");
    }
  };

  // Issue a certificate and get the ID from the event
  const issueCertificate = async () => {
    try {
      setIssueStatus("Issuing...");
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contractWithSigner = new ethers.Contract(contractAddress, abi, signer);
      const contractWithProvider = new ethers.Contract(contractAddress, abi, provider);

      // Get the next certificate ID before issuing (read-only)
      const nextId = await contractWithProvider.nextCertificateId();

      const tx = await contractWithSigner.issueCertificate(recipient, uri);
      await tx.wait();

      setIssueStatus(`Certificate issued! ID: ${nextId.toString()}`);
    } catch (err) {
      setIssueStatus("Error: " + (err?.info?.error?.message || err.message));
    }
  };

  // Verify a certificate
  const verifyCertificate = async () => {
    try {
      const provider = new ethers.BrowserProvider(window.ethereum);
      const contract = new ethers.Contract(contractAddress, abi, provider);
      const owner = await contract.ownerOf(verifyId);
      const tokenUri = await contract.tokenURI(verifyId);
      setVerifyResult({ owner, tokenUri });
    } catch (err) {
      setVerifyResult({ error: "Certificate not found or error: " + (err?.info?.error?.message || err.message) });
    }
  };

  return (
    <div className="App">
      <header className="App-header">
        <h2>Blockchain Certificate Issuer</h2>
        {!account ? (
          <button onClick={connectWallet}>Connect Wallet</button>
        ) : (
          <div className="wallet-info">
            Connected: {account}
          </div>
        )}

        <div className="card">
          <h3>Issue Certificate</h3>
          <input
            type="text"
            placeholder="Recipient Address"
            value={recipient}
            onChange={e => setRecipient(e.target.value)}
          />
          <input
            type="text"
            placeholder="Certificate Metadata URI"
            value={uri}
            onChange={e => setUri(e.target.value)}
          />
          <button onClick={issueCertificate}>Issue Certificate</button>
          {issueStatus && (
            <div className={`status-message ${issueStatus.includes('Error') ? 'error' : 'success'}`}>
              {issueStatus}
            </div>
          )}
        </div>

        <div className="card">
          <h3>Verify Certificate</h3>
          <input
            type="number"
            placeholder="Certificate ID"
            value={verifyId}
            onChange={e => setVerifyId(e.target.value)}
          />
          <button onClick={verifyCertificate}>Verify</button>
          {verifyResult && (
            <div className="verify-result">
              {verifyResult.error ? (
                <div className="error">{verifyResult.error}</div>
              ) : (
                <div>
                  <div><b>Owner:</b> {verifyResult.owner}</div>
                  <div><b>Metadata URI:</b> {verifyResult.tokenUri}</div>
                </div>
              )}
            </div>
          )}
        </div>
      </header>
    </div>
  );
}

export default App;
